namespace IntroLes
{
    /// <summary>
    /// An empty page that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class MainPage : Page
    {
        public MainPage()
        {
            this.InitializeComponent();
        }

        private async void button_Click(object sender, RoutedEventArgs e)
        {
            var text = string.Format("Hello {0}!", txtName.Text);
            ShowDialog(text);
        }

        private async void SayText(string text)
        {
            MediaElement mediaElement = new MediaElement();
            var synth = new Windows.Media.SpeechSynthesis.SpeechSynthesizer();
            Windows.Media.SpeechSynthesis.SpeechSynthesisStream stream = await synth.SynthesizeTextToStreamAsync(text);
            mediaElement.SetSource(stream, stream.ContentType);
            mediaElement.Play();
        }

        private async void ShowDialog(string text)
        {
            var messageDialog = new MessageDialog(text);
            messageDialog.Title = "Welcome";
            await messageDialog.ShowAsync();
        }
    }
}
